    private void removeAddrButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeAddrButtonActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this address?", "Remove Address", JOptionPane.YES_NO_OPTION);
        
        if(result == JOptionPane.YES_OPTION){
            int id = addressList.getSelectedIndex();
            model.removeElementAt(id);
            am.deleteAddress(id + 1);
        }
    }//GEN-LAST:event_removeAddrButtonActionPerformed

