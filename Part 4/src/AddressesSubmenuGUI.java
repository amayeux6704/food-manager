    private void initializeAddresses(){
        int numAddresses = am.getNumAddresses();
        
        for(int i = 1; i <= numAddresses; i++){
            Address addr = am.searchAddress(i);
            
            if(addr != null){
                String addrInfo = addr.stringAddressInformation();
                model.addElement(addrInfo);
            }
        }
    }
    
    private void refreshAddresses(){
        
        int numAddresses = am.getNumAddresses();
        
        for(int i = 1; i <= numAddresses; i++){
            Address addr = am.searchAddress(i);
            
            if(addr != null){
                String addrInfo = addr.stringAddressInformation();
                model.set(i - 1, addrInfo);
            }
        }
        
        addressList.updateUI();
    }
    
