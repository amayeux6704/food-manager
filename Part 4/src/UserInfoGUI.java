    private boolean updateUserInfo(){
        updateName();
        updateEmail();
        if(changePWChkBx.isSelected())
            return updatePassword();
        else
            return true;
    }
    
    private void updateName(){
        String name = nameTF.getText();
        person.setName(name);

    }
    
    private void updateEmail(){
        String email = emailTF.getText();
        person.setEmail(email);
    }
    
    private boolean updatePassword(){
        String oldPassword = new String(currPWField.getPassword());
        
        if(person.getPassword().equals(oldPassword)){
            String newPassword = new String(newPWField.getPassword());
            String newPassword1 = new String(reEnterNewPWField.getPassword());
            
            if(newPassword.equals(newPassword1)){
                person.setPassword(newPassword);
                return true;
            }
            else{
                JOptionPane.showMessageDialog(this, "The two passwords do not match.");
                return false;
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Incorrect password.");
            return false;
        }
    }
    
    private void enableDisablePWChange(boolean enable){
        passwordPanel.setEnabled(enable);
        enterCurrPWLabel.setEnabled(enable);
        currPWField.setEnabled(enable);
        enterNewPWLabel.setEnabled(enable);
        newPWField.setEnabled(enable);
        reEnterNewPWLabel.setEnabled(enable);
        reEnterNewPWField.setEnabled(enable); 
    }
    
