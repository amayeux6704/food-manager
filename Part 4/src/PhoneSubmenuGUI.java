    private void removePhnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removePhnButtonActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove this Phone Number?", "Remove Phone Number", JOptionPane.YES_NO_OPTION);
        
        if(result == JOptionPane.YES_OPTION){
            String phoneNumber= phoneList.getSelectedValue();
            model.removeElement(phoneNumber);
            pm.removePhoneNumber(phoneNumber);
        }
    }//GEN-LAST:event_removePhnButtonActionPerformed

