    private void showPhoneNumbers(){
        model = new DefaultListModel();
        Set<String> phoneNumbers = pm.getPhoneNumbers();
        
        for(String phoneNumber: phoneNumbers){
            model.addElement(phoneNumber);
        }
        
        phoneList.setModel(model);
        phoneList.updateUI();
    }
    
