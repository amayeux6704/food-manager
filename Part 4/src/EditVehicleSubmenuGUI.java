    private void setFields(){
        yearCBox.setSelectedItem(String.valueOf(vehicle.getYear()));
        makeTF.setText(vehicle.getMake());
        modelTF.setText(vehicle.getModel());
        bodyStyleTF.setText(vehicle.getBodyStyle());
        colorTF.setText(vehicle.getColor());
        licensePlateTF.setText(vehicle.getLicensePlate());
    }
    
    private void setVehicle(){
        vehicle.setYear(Short.parseShort((String) yearCBox.getSelectedItem()));
        vehicle.setMake(makeTF.getText());
        vehicle.setModel(modelTF.getText());
        vehicle.setBodyStyle(bodyStyleTF.getText());
        vehicle.setColor(colorTF.getText());
        vehicle.setLicensePlate(licensePlateTF.getText());
    }
    
    private boolean hasChanged(){
        if(vehicle != null){
            return !(vehicle.getYear() == (Short.parseShort((String) yearCBox.getSelectedItem())) &&
                    vehicle.getMake().equals(makeTF.getText()) &&
                    vehicle.getModel().equals(modelTF.getText()) &&
                    vehicle.getBodyStyle().equals(bodyStyleTF.getText()) &&
                    vehicle.getColor().equals(colorTF.getText()) &&
                    vehicle.getLicensePlate().equals(licensePlateTF.getText()));
        }
        else
            return false;
    }
    
