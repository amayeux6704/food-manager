    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:
        if(hasChanged()){
            int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to apply these changes?", "Apply Changes", JOptionPane.YES_NO_OPTION);
        
            if(result == JOptionPane.YES_OPTION){
                setVehicle();
                this.dispose();
            }
        }
        else{
            this.dispose();
        }
    }//GEN-LAST:event_okButtonActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        // TODO add your handling code here:
        if(hasChanged()){
            int result = JOptionPane.showConfirmDialog(this, "Are you sure you want to cancel?", "Cancel", JOptionPane.YES_NO_OPTION);
        
            if(result == JOptionPane.YES_OPTION){
                this.dispose();
            }
        }
        else{
            this.dispose();
        }
    }//GEN-LAST:event_cancelButtonActionPerformed

